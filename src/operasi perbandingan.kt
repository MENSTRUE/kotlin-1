fun main() {
    /* simbol operator perbandingan
    >       =       lebih dari
    <       =       kurang dari
    >=      =       lebih dari sama dengan
    <=      =       kurang dari sama dengan
    ==      =       sama dengan
    !=      =       tidak sama dengan
*/

    val a = 100
    val b = 300

    val result: Boolean = a > b

    println(result)

    println(100 >= 100)
    println(100 <= 100)

    println(a != b)
    println("wafa" == "manusia")
}
