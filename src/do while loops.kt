fun main() {
    /*
     pada Do While Loop, kode blok akan dijalankan dahulu, baru diakhir dilakukan pengecekan kondisi
     */

    var i = 2

    do{

        println("perulangan ke $i") // jika hasilnya false maka dia akan sekali perulangan beda halnya dengan while dia tidak akan mengulang
        i++

    }while (i<10)

}